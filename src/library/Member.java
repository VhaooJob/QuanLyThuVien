    public void Member_load()
    {
        int a;
        try {
            pst = con.prepareStatement("select*from member");
            rs = pst.executeQuery();
            
            ResultSetMetaData rsd = rs.getMetaData();
            a = rsd.getColumnCount();
            
            
            DefaultTableModel d = (DefaultTableModel)viewMember.getModel();
            d.setRowCount(0);
            
            while(rs.next())
            {
                Vector v2 = new Vector();
                
                for(int i=1; i<=a;i++)
                {
                    v2.add(rs.getString("id"));
                    v2.add(rs.getString("name"));
                    v2.add(rs.getString("phone"));
                    v2.add(rs.getString("address"));
                }
                  d.addRow(v2);
                 String id=rs.getString(1);
                String name =rs.getString(2);
                String phone =rs.getString(3);
                  String address =rs.getString(4);
                SearchMember member = new SearchMember(id , name ,phone,address );
                arr.add(member);
            }
            
            
        } catch (SQLException ex) {
            Logger.getLogger(Member.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
