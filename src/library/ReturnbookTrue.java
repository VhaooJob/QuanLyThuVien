    public void Returnbook_load()
    {
         LoaderArayListTB(); 
        int c;
        try {
            pst = con.prepareStatement("select rb.id,rb.mname,rb.bname,rb.returndate,rb.elp,rb.fine,rb.mname from returnbook rb where rb.fine > 0 and rb.elp > 0 ");
            rs = pst.executeQuery();
            
            ResultSetMetaData rsd = rs.getMetaData();
            c = rsd.getColumnCount();
            
            
            DefaultTableModel d = (DefaultTableModel)viewReturnbook.getModel();
            d.setRowCount(0);
            
            while(rs.next())
            {
                Vector v2 = new Vector();
                
                for(int i=1; i<=c;i++)
                {
                   v2.add(rs.getString("rb.id"));
                    v2.add(rs.getString("rb.mname"));
                    v2.add(rs.getString("rb.bname"));
                    v2.add(rs.getString("rb.returndate"));
                    v2.add(rs.getString("rb.elp"));
                    v2.add(rs.getString("rb.fine"));
                }
                
               d.addRow(v2);
                String id=rs.getString(1);
                String mid =rs.getString(2);
                String mname =rs.getString(3);
                String bname =rs.getString(4);
                String returndate =rs.getString(5);
                String elp =rs.getString(6);
                String fine =rs.getString(7);
                SearchReturnBook rb = new SearchReturnBook(id , mid ,mname,bname,returndate,elp,fine );
                arr.add(rb);
              
            }
             
            
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }
