    private void txtmidKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtmidKeyPressed
        // TODO add your handling code here:
        if(evt.getKeyCode()==KeyEvent.VK_ENTER)
        {
            String id = txtmid.getText();

            try {
                pst = con.prepareStatement("select m.name , l.id, b.bname, l.returndate, DATEDIFF(now(),l.returndate) as elap from lendbook l JOIN member m ON l.memberid = m.id JOIN book b ON l.bookid = b.id and l.memberid = ? ");
                pst.setString(1, id);
                rs = pst.executeQuery();
                if(rs.next()==false)
                {
                JOptionPane.showMessageDialog(this, "Member ID not found");

                }
                else
                {
                    String mname = rs.getString("m.name");
                    String lssuaid = rs.getString("l.id");
                    String bname = rs.getString("b.bname");

                    txtmname.setText(mname.trim());
                    txtbook.setText(bname.trim());
                    txtIssuaid.setText(lssuaid.trim());
                    
                    String date = rs.getString("l.returndate");
                    txtrdate.setText(date);
                    
                    String elp = rs.getString("elap");
                    int elaped = Integer.parseInt(elp);
                    if(elaped > 0)
                    {
                        txtelp.setText(elp);
                        int fine = elaped * 1000;
                        txtfine.setText(String.valueOf(fine));
                    }
                    else
                    {
                        txtelp.setText("0");
                        txtfine.setText("0");
                    }
                }

            } catch (SQLException ex) {
                Logger.getLogger(Returnbook.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
       
    }//GEN-LAST:event_txtmidKeyPressed

